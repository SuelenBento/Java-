package Exercicios26_01;

import java.util.Scanner;

/* 8. O custo ao consumidor de um carro novo é a soma do custo de fábrica com a percentagem do distribuidor e dos impostos
  (aplicados ao custo de fábrica). Supondo que a percentagem do distribuidor seja de 28% e os impostos de 45%,
   escrever um sistema que leia o custo de fábrica de um carro e escreva o custo ao consumidor.*/

public class Exercicio08 {

	public static void main(String[] args) {
		
		Scanner leia = new Scanner(System.in);
		
		  double custConsumidor, custFabrica, porcDistribuidor, impostos;
		  
		
		  System.out.println("Dígite o custo de fabrica: ");
		  custFabrica = leia.nextDouble();
		  
		  porcDistribuidor = (custFabrica * 28) /100;
		  
		  impostos = (custFabrica * 48 ) /100;
		  
		  custConsumidor = custFabrica +  impostos + porcDistribuidor;
		  
		  System.out.printf("O carro apreço do consumidor é de :  %.2f", custConsumidor , " reais");
		  
		  leia.close();
		  
		  
				  
		
		

	}

}
